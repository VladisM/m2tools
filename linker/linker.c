static void print_version(void);
static void print_help(void);
static void arg_parse(int argc, char* argv[]);
static void clean_mem(void);

#ifndef NDEBUG
void print_settings(void);
#endif

typedef struct{
    char **libs;
    char **obj_files;
    char *output_filename;
    char *linker_script;
    unsigned int libs_count;
    unsigned int obj_count;
    bool strip_unused_sections;
}settings_t;

static settings_t settings;
ldm_file_t *finalLDM = NULL;
lds_t *lds = NULL;

#define HELP_STRING "\
Example usage: \n\
 "PROG_NAME" -o out.ldm -T linker.ld -l my_lib.sl main.o\n\
\n\
        This is linker for "TARGET_ARCH_NAME" CPU that can be used\n\
    to link multiple object files into one executable file. Linker\n\
    also does support static libraries generated by archiver.\n\
\n\
Arguments:\n\
    -h --help           Print this help.\n\
       --version        Print version number and exit.\n\
    -l                  Link static library, have to be followed by path.\n\
    -o --output         Set output filename, have to be followed by name.\n\
    -T                  Path to the linker script.\n\
       --strip-unused   Put unused sections away. Strip down output size.\n\
\n"

int main(int argc, char *argv[]){

    atexit(clean_mem);

    #ifndef NDEBUG
    printf("Running debug version of "PROG_NAME" version "VERSION".\n");
    printf("Parsing args...\n");
    #endif

    //get arguments
    arg_parse(argc, argv);

    #ifndef NDEBUG
    print_settings();
    printf("args OK\n\n");
    printf("Parsing linker script file...\n");
    #endif

    // parse linker script
    lds = parse_lds(settings.linker_script);

    #ifndef NDEBUG
    print_lds(lds);
    printf("lds OK\n\n");
    printf("Create LDM file structure...\n");
    #endif

    //create LDM structure
    if(new_ldm_file(&finalLDM, settings.output_filename) != true){
        fprintf(stderr, "Failed to create LDM file struct! ldmlib errno: %d\n", get_ldmlib_errno());
        exit(EXIT_FAILURE);
    }

    if(lds->first_mem == NULL){
        fprintf(stderr, "There is no memory defined in the LDS file!\n");
        exit(EXIT_FAILURE);
    }

    for(mem_t *head = lds->first_mem; head != NULL; head = head->next){
        ldm_mem_t *tmp_mem = NULL;

        if(new_mem(&tmp_mem, head->name, head->orig, head->size) != true){
            fprintf(stderr, "Failed to create memory item %s. ldmlib errno: %d\n", head->name, get_ldmlib_errno());
            exit(EXIT_FAILURE);
        }

        if(append_mem_into_file(tmp_mem, finalLDM) != true){
            fprintf(stderr, "Failed to append memory item %s into LDM file! ldmlib errno: %d\n", head->name, get_ldmlib_errno());
            exit(EXIT_FAILURE);
        }
    }

    #ifndef NDEBUG
    printf("ldm OK\n\n");
    printf("Create section cashe...\n");
    #endif

    //create cashe with all sections - later we will work with that cashe
    //first - object files
    for(unsigned int i = 0; i < settings.obj_count; i++){
        obj_file_t *tmp_obj = NULL;

        if(!obj_load_from_file(settings.obj_files[i], &tmp_obj)){
            fprintf(stderr, "Failed to load obj file '%s'! objlib errno: %d\n", settings.obj_files[i], get_objlib_errno());
            exit(EXIT_FAILURE);
        }

        if(!append_into_section_list_obj(tmp_obj)){
            fprintf(stderr, "Failed to append object file '%s' into cashe! ", settings.obj_files[i]);

            if(get_section_list_errno() == SECTION_OBJLIB_ERROR)
                fprintf(stderr, "Error in objlib, errno: %d\n", get_objlib_errno());
            else if(get_section_list_errno() == SECTION_ISALIB_ERROR)
                fprintf(stderr, "Error in isalib, errno: %d\n", get_isalib_errno());
            else
                fprintf(stderr, "section list errno: %d\n", get_section_list_errno());

            exit(EXIT_FAILURE);
        }

        free_object_file(tmp_obj);
    }

    //then libraries
    for(unsigned int i = 0; i < settings.libs_count; i++){
        static_library_t *tmp_sl = NULL;

        if(!sl_load(settings.libs[i], &tmp_sl)){
            fprintf(stderr, "Failed to load static library '%s'! sllib errno: %d\n", settings.libs[i], get_sllib_errno());
            exit(EXIT_FAILURE);
        }

        if(!append_into_section_list_sl(tmp_sl)){
            fprintf(stderr, "Failed to append static library '%s' into cashe! ", settings.libs[i]);

            if(get_section_list_errno() == SECTION_OBJLIB_ERROR)
                fprintf(stderr, "Error in objlib, errno: %d\n", get_objlib_errno());
            else if(get_section_list_errno() == SECTION_ISALIB_ERROR)
                fprintf(stderr, "Error in isalib, errno: %d\n", get_isalib_errno());
            else
                fprintf(stderr, "section list errno: %d\n", get_section_list_errno());

            exit(EXIT_FAILURE);
        }

        free_sl(tmp_sl);
    }

    #ifndef NDEBUG
    print_section_list();
    printf("cashe OK\n\n");
    printf("Building symbol table...\n");
    #endif

    //build symbol table
    for(section_list_item_t *head = first_section_item; head != NULL; head = head->next){
        if(!parse_symbols(head)){
            if(get_symbol_list_errno() == SYMBOLLIST_OBJLIB_ERROR)
                fprintf(stderr, "Error in objlib from symbol list when building symbol table.\nobjlib errno: %d\n", get_objlib_errno());
            else
                fprintf(stderr, "Error in symbol list when building symbol table.\nerrno: %d\n", get_symbol_list_errno());

            exit(EXIT_FAILURE);
        }
    }

    //also put into symbol table symbols from linker script
    if(!parse_linker_symbols(lds)){
        if(get_symbol_list_errno() == SYMBOLLIST_OBJLIB_ERROR)
            fprintf(stderr, "Error in objlib from symbol list when parsing symbols from linker script.\nobjlib errno: %d\n", get_objlib_errno());
        else
            fprintf(stderr, "Error in symbol list when parsing symbols from linker script.\nerrno: %d\n", get_symbol_list_errno());

        exit(EXIT_FAILURE);
    }

    #ifndef NDEBUG
    print_symbols_lists();
    printf("table OK\n\n");
    printf("Checking if we have all symbols and marking sections...\n");
    #endif

    //check if symbols are closet set and mark unused sections
    if(!check_imported_symbols_exist(lds->entry_point)){
        fprintf(stderr, "Some symbols are missing, provide their declaration!\n");
        exit(EXIT_FAILURE);
    }

    //mark section with symbol from lds entry point as used
    if(!mark_section_with_symbol_as_used(lds->entry_point)){
        fprintf(stderr, "Error! Failed to mark section with symbol '%s' as used!\n", lds->entry_point);
        fprintf(stderr, "Section list errno: %d\n", get_section_list_errno());
        exit(EXIT_FAILURE);
    }

    #ifndef NDEBUG
    print_section_status();
    printf("Section marking OK\n\n");
    printf("Remove unused sections...\n");
    #endif

    //remove unused sections
    if(settings.strip_unused_sections == true){
        if(!strip_unused_sections()){
            fprintf(stderr, "Failed to take out unused section! Errno: %d\n", get_section_list_errno());
            exit(EXIT_FAILURE);
        }
    }

    #ifndef NDEBUG
    print_section_status();
    printf("unused OK\n\n");
    printf("allocating sections into memories...\n");
    #endif

    //put sections into memories - calculating their offset in memory
    for(section_list_item_t *head = first_section_item; head != NULL; head = head->next){
        char *found_mem_name = NULL;

        for(mem_t *mem_head = lds->first_mem; mem_head != NULL; mem_head = mem_head->next){
            if(is_section_in_mem(head->section->section_name, mem_head) == true){
                found_mem_name = mem_head->name;
                break;
            }
            else{
                if(mem_head == lds->last_mem){
                    fprintf(stderr, "Error! Section '%s' is not assigned into any memory!\n", head->section->section_name);
                    exit(EXIT_FAILURE);
                }
            }
        }

        for(ldm_mem_t *mem_head = finalLDM->first_mem; mem_head != NULL; mem_head = mem_head->next){
            if(strcmp(found_mem_name, mem_head->mem_name) == 0){
                head->assinged_mem = mem_head;
                break;
            }
            else{
                if(mem_head == finalLDM->last_mem){
                    fprintf(stderr, "Error! Didn't found ldm_mem but corresponding mem_t exist!\n");
                    fprintf(stderr, "Name of mem_t: %s \n", found_mem_name);
                    exit(EXIT_FAILURE);
                }
            }
        }

        head->begin_addr = head->assinged_mem->last_offset;
        head->assinged_mem->last_offset += get_section_size(head->section);
    }

    #ifndef NDEBUG
    print_section_list();
    printf("allocating OK\n\n");
    #endif

    printf("Exit!\n");
    return 1;
}


static void print_version(void){
    printf("linker for %s CPU %s\n", TARGET_ARCH_NAME, VERSION);
}

static void print_help(void){
    printf(HELP_STRING);
}

static void arg_parse(int argc, char* argv[]){

    settings.libs = NULL;
    settings.libs_count = 0;
    settings.obj_files = NULL;
    settings.obj_count = 0;
    settings.output_filename = NULL;
    settings.linker_script = NULL;
    settings.strip_unused_sections = false;

    for(int i = 1; i<argc; i++){
        if(strcmp(argv[i], "-h") == 0 || strcmp(argv[i], "--help") == 0 ){
            print_help();
            exit(EXIT_SUCCESS);
        }
        else if(strcmp(argv[i], "--version") == 0 ){
            print_version();
            exit(EXIT_SUCCESS);
        }
        else if(strcmp(argv[i], "-o") == 0 || strcmp(argv[i], "--output") == 0){
            if(settings.output_filename != NULL){
                fprintf(stderr, "Too much output files given!\n");
                exit(EXIT_FAILURE);
            }
            else if((i+1)<argc){
                settings.output_filename = argv[++i];
            }
            else{
                fprintf(stderr, "Wrong arg format!\n");
                exit(EXIT_FAILURE);
            }
        }
        else if(strcmp(argv[i], "-T") == 0){
            if(settings.linker_script != NULL){
                fprintf(stderr, "Too much output files given!\n");
                exit(EXIT_FAILURE);
            }
            else if ((i+1)<argc){
                settings.linker_script = argv[++i];
            }
            else{
                fprintf(stderr, "Wrong arg format!\n");
                exit(EXIT_FAILURE);
            }
        }
        else if(strcmp(argv[i], "-l") == 0){
            if((i+1)<argc){
                char **new_ptr = (char **) realloc((void **)settings.libs, sizeof(char **) * (settings.libs_count + 1));

                if(new_ptr != NULL){
                    settings.libs = new_ptr;
                    settings.libs[settings.libs_count++] = argv[++i];
                }
                else{
                    fprintf(stderr, "Realloc failed!\n");
                    exit(EXIT_FAILURE);
                }
            }
            else{
                fprintf(stderr, "Wrong arg format!\n");
                exit(EXIT_FAILURE);
            }
        }
        else if(strcmp(argv[i], "--strip-unused") == 0 ){
            settings.strip_unused_sections = true;
        }
        else{
            if(*(argv[i]) == '-'){
                fprintf(stderr, "Wrong arg format!\n");
                exit(EXIT_FAILURE);
            }
            else{
                char **new_ptr = (char **) realloc((void **)settings.obj_files, sizeof(char **) * (settings.obj_count + 1));

                if(new_ptr != NULL){
                    settings.obj_files = new_ptr;
                    settings.obj_files[settings.obj_count++] = argv[i];
                }
                else{
                    fprintf(stderr, "Realloc failed!\n");
                    exit(EXIT_FAILURE);
                }
            }
        }
    }

    if(settings.linker_script == NULL){
        fprintf(stderr, "Missing linker script path!\n");
        exit(EXIT_FAILURE);
    }

    if(settings.obj_count == 0 && settings.libs_count == 0){
        fprintf(stderr, "Given zero libs and zero object, nothink to link!\n");
        exit(EXIT_FAILURE);
    }

    if(settings.output_filename == NULL){
        fprintf(stderr, "Missing output filename!\n");
        exit(EXIT_FAILURE);
    }

}

static void clean_mem(void){
    if(settings.obj_files != NULL){
        free(settings.obj_files);
    }

    if(settings.libs != NULL){
        free(settings.libs);
    }

    if(finalLDM != NULL){
        free_ldm_file_struct(finalLDM);
    }

    if(lds != NULL){
        free_lds(lds);
    }

    if(first_section_item != NULL){
        clean_up_section_list();
    }

    if(exported_symbols != NULL || imported_symbols != NULL){
        clean_up_symbol_lists();
    }
}

#ifndef NDEBUG
void print_settings(void){
    printf("Settings:\n");

    printf("|- Linker script: '%s'\n", settings.linker_script);
    printf("|- Output name: '%s'\n", settings.output_filename);
    printf("|- Strip unused: '%s'\n", settings.strip_unused_sections ? "true" : "false");
    if(settings.libs_count == 0){
        printf("|- Static libs: (null)\n");
    }
    else{
        printf("|- Static libs: %d\n", settings.libs_count);

        for(unsigned int i = 0; i < settings.libs_count - 1; i++){
            printf("|  |- '%s'\n", settings.libs[i]);
        }
        printf("|  '- '%s'\n", settings.libs[settings.libs_count - 1]);
    }

    if(settings.obj_count == 0){
        printf("'- Object files: (null)\n");
    }
    else{
        printf("'- Object files: %d\n", settings.obj_count);

        for(unsigned int i = 0; i < settings.obj_count - 1; i++){
            printf("   |- '%s'\n", settings.obj_files[i]);
        }
        printf("   '- '%s'\n", settings.obj_files[settings.obj_count - 1]);
    }
}
#endif
